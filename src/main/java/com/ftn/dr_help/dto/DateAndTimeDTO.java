package com.ftn.dr_help.dto;

public class DateAndTimeDTO {

	private String dateAndTimeString;
	
	public DateAndTimeDTO() {
		super();
	}

	public DateAndTimeDTO(String dateAndTime) {
		super();
		this.dateAndTimeString = dateAndTime;
	}

	public String getDateAndTimeString() {
		return dateAndTimeString;
	}

	public void setDateAndTimeString(String dateAndTimeString) {
		this.dateAndTimeString = dateAndTimeString;
	}
	
	
	
	
}
